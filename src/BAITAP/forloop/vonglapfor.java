package BAITAP.forloop;

public class vonglapfor {
    public static void main(String[] args){
        // Tạo ra vòng lặp FOR để in ra các số chẵn chạy từ 0-50
        // FOR Đơn giản
        for (int i = 0; i <= 50; i++){
            if (i % 2 == 0) {
                System.out.print(i + " ");
            }
        }
    }
}
