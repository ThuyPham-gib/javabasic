package javabasic.vonglapfor;

public class basicVongLapFor {
    public static void main(String[] args){
        for (int i = 1; i <= 10; i ++){// nếu thay i ++ = i+2 thì sẽ nhảy + thêm 2 (ví dụ 1 nhảy đến 3 vì 1+2=3)
            System.out.println(i);
            // in thêm lệnh gì thì sẽ lặp lại lệnh đó
        }
    }
}
